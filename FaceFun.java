public class FaceFun {

    public static void main(String[] args) {
        
        System.out.println("  \n  //////"); // Hair
        System.out.println(" |     |");  // Forehead
        System.out.println(" | \" \" |");  // Eyebrows
        System.out.println(" | 0 0 |"); // Eyes
        System.out.println("(|  ^  |)"); // Ears
        System.out.println(" | \\_/ |"); // Mouth
        System.out.println(" |_____|"); // Chin

    }
}
